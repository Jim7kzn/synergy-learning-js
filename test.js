/*  Программа для вычисления факториала числа/
	определяем функцию, в аргументе одна переменная
	приводим к числовому виду,
	задаём значение по умолчанию.
 */
let myFactorial = function (nVal = 1) {
	let res = 1; 	 				// задаём начальное значение

	for(nVal; nVal >= 2; nVal--)	// цикл перебора значений
		res *= nVal; 				// последовательное умножение

	return res;		 	 			// возвращаем результат.
}

console.log(myFactorial(6)); 	//выводим значение в терминал.
